#!/bin/bash

# Проверяем, передан ли аргумент
if [ $# -eq 0 ]; then
    echo "Ошибка: Укажите название файла"
    echo "Использование: $0 <название файла>"
    exit 1
fi

# Получаем аргумент и заменяем пробелы на подчеркивания
filename=$(echo "$*" | tr ' ' '_').sql

# Создаем файл
touch "$filename"

echo "Файл создан: $filename"